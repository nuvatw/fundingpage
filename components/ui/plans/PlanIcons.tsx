// Helper component to render icon by name
export function PlanIcon({
  name,
  className,
}: {
  name: string;
  className?: string;
}) {
  const IconComponent = PLAN_ICON_MAP[name];
  if (!IconComponent) return null;
  return <IconComponent className={className} />;
}
